# Exemplo de if
dia = 'segunda'
if dia == 'segunda'
    puts 'Hoje é segunda'
end

#Exemplo de if else
dia = 'segunda'
if dia == 'segunda'
    puts 'Hoje é segunda'
else
    puts 'Hoje não é segunda'
end

#Exemplo de elsif
dia = 'terça'
if dia == 'segunda'
    puts 'Hoje é segunda'
elsif dia == 'terça'
    puts 'Hoje é terça'
else
    puts 'Hoje não é segunda nem terça'
end

#Exemplo de Unless
valor = 11
unless valor > 10
    puts 'Maior que 10'
else
    puts 'Menor que 10'
end